package kr.co.otj.beans;

public class EmployeeSkillBean {
	private int emp_skill_num;
	private int emp_num;
	private String skill_cd;
	
	
	public int getEmp_skill_num() {
		return emp_skill_num;
	}
	public void setEmp_skill_num(int emp_skill_num) {
		this.emp_skill_num = emp_skill_num;
	}
	public int getEmp_num() {
		return emp_num;
	}
	public void setEmp_num(int emp_num) {
		this.emp_num = emp_num;
	}
	public String getSkill_cd() {
		return skill_cd;
	}
	public void setSkill_cd(String skill_cd) {
		this.skill_cd = skill_cd;
	}
}
